def main() -> None:
    login = request_json("POST", "/api/auth/login", data={"username": "admin", "password": "12345678"})
    token = login["access_token"]
    kbs = request_json("GET", "/api/knowledge-bases", token=token)
    kb_map = {item["code"]: item for item in kbs.get("items", [])}

    results: list[dict] = []
    for code, docs in DOCUMENTS.items():
        kb = kb_map.get(code)
        if not kb:
            results.append({"code": code, "error": "knowledge base not found"})
            continue
        for title, content in docs:
            try:
                resp = upload_document(token, kb["id"], title, content)
                results.append(
                    {
                        "kb": kb["name"],
                        "title": title,
                        "chars": len(content),
                        "document_id": resp.get("document_id") or resp.get("id"),
                        "index_status": resp.get("index_status"),
                    }
                )
            except urllib.error.HTTPError as exc:
                detail = exc.read().decode("utf-8", errors="replace")
                results.append({"kb": kb["name"], "title": title, "error": detail})

    for code, (title, content) in SKILL_MANUALS.items():
        kb = kb_map.get(code)
        if not kb:
            continue
        try:
            resp = upload_document(token, kb["id"], title, content)
            results.append(
                {
                    "kb": kb["name"],
                    "title": title,
                    "chars": len(content),
                    "document_id": resp.get("document_id") or resp.get("id"),
                    "index_status": resp.get("index_status"),
                    "kind": "skill-manual",
                }
            )
        except urllib.error.HTTPError as exc:
            detail = exc.read().decode("utf-8", errors="replace")
            results.append(
                {
                    "kb": kb["name"],
                    "title": title,
                    "error": detail,
                    "kind": "skill-manual",
                }
            )

    print(json.dumps(results, ensure_ascii=False, indent=2))
    final = request_json("GET", "/api/knowledge-bases", token=token)
    print("\nKB counts:")
    for item in final.get("items", []):
        print(f"- {item['name']}: {item.get('document_count')} docs")

    skills = request_json("GET", "/api/skills", token=token)
    print("\nSkills:")
    for item in skills.get("items", []):
        print(
            f"- {item.get('name')}: enabled={item.get('enabled')} implemented={item.get('implemented')} desc={item.get('description')}"
        )


