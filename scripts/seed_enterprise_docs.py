BASE = "http://127.0.0.1:8000"


def request_json(method: str, path: str, token: str | None = None, data: dict | None = None):
    headers: dict[str, str] = {}
    body = None
    if token:
        headers["Authorization"] = f"Bearer {token}"
    if data is not None:
        headers["Content-Type"] = "application/json"
        body = json.dumps(data, ensure_ascii=False).encode("utf-8")
    req = urllib.request.Request(BASE + path, data=body, headers=headers, method=method)
    with urllib.request.urlopen(req) as resp:
        raw = resp.read().decode("utf-8")
        return json.loads(raw) if raw else None


def upload_document(token: str, kb_id: str, title: str, content: str) -> dict:
    boundary = f"----Boundary{uuid.uuid4().hex}"
    chunks: list[bytes] = []

    def add_field(name: str, value: str) -> None:
        chunks.append(f"--{boundary}\r\n".encode())
        chunks.append(
            f'Content-Disposition: form-data; name="{name}"\r\n\r\n{value}\r\n'.encode("utf-8")
        )

    def add_file(name: str, filename: str, value: bytes, content_type: str) -> None:
        chunks.append(f"--{boundary}\r\n".encode())
        chunks.append(
            (
                f'Content-Disposition: form-data; name="{name}"; filename="{filename}"\r\n'
                f"Content-Type: {content_type}\r\n\r\n"
            ).encode("utf-8")
        )
        chunks.append(value)
        chunks.append(b"\r\n")

    add_file("file", f"{title}.txt", content.encode("utf-8"), "text/plain")
    add_field("title", title)
    chunks.append(f"--{boundary}--\r\n".encode())
    body = b"".join(chunks)

    headers = {
        "Authorization": f"Bearer {token}",
        "Content-Type": f"multipart/form-data; boundary={boundary}",
    }
    req = urllib.request.Request(
        f"{BASE}/api/knowledge-bases/{kb_id}/documents",
        data=body,
        headers=headers,
        method="POST",
    )
    with urllib.request.urlopen(req) as resp:
        return json.loads(resp.read().decode("utf-8"))


def doc(text: str) -> str:
    content = textwrap.dedent(text).strip() + "\n"
    if len(content) < 300:
        raise ValueError(f"document too short: {len(content)}")
    return content


